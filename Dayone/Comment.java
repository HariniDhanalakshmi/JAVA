//Comment types
public class Comment{
	public static void main(String[] args){
		
		System.out.println("Heyy there!!!");//This is single line comment used for short comments
		//Single-line comments start with two forward slashes (//).
		
		System.out.println("Multi line comment starts with /*");
		System.out.println("Multi line comment ends with */");
		/*this is to show the multi line comment sample used for long comments*/
	}
}
		
//java numbers
public class numbers{
	public static void main(String[] args){
		byte a = 100;//whole numbers from -128 to 127
		short b = 5000;//store whole numbers from -32768 to 32767
		int c = 100000;// store whole numbers from -2147483648 to 2147483647
		long d = 1500000005463636403L;// store whole numbers from -9223372036854775808 to 9223372036854775807
		float e = 5234442.254f; // 6 or 7 decimal digits
		double g = 1435684.94d;//16 decimal digits
		//Scientfitic numbers
		float f1 = 35e3f;
		double d1 = 12E4d;
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(g);
		System.out.println(f1);
		System.out.println(d1);
		
		//boolean types
		boolean good = true;
		boolean bad = false;
		System.out.println(bad);
		System.out.println(good);
		
	}
}
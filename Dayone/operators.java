public class operators{
	public static void main(String args[]){
		int a = 4;
		int b = 54;
		//adding values of 2 var
		int c = a+b;
		//add value and var
		int d = 7+a;
		int e = c+d;
		//adding two values
		int f = 53+43;
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
	}
}


public class Comparison{
		public static void main(String[] args){
			int a = 1;
			int b = 4;
			System.out.println(a==b);/*check its equal or not , returns true if it is equal
            returns false if it is not equal*/			
			System.out.println(a!=b);
			//returns true because a is not equal to b
			System.out.println(a>b);
			//returns false because a is smaller than b
			System.out.println(a<b);
			//returns true because a is smaller than b
			System.out.println(a>=b); // returns false because a is smaller, or equal, to b
			System.out.println(a <= b); // returns true because a is smaller, or equal, to b 
			
		}
		}
			
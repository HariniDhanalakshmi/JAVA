public class specialchar{
	public static void main(String[] args){
	String  line = "This is a line that contains sample of \"Double quote\"";/*double qoute in string*/
	System.out.println(line);
	
	String line1 = "This contains sample of single quote it\'s";/*single quote in string*/
	System.out.println(line1);
	
	String line2 = "to show this \\ that";//to show slash
    System.out.println(line2);
	
	System.out.println("Hello I'm Harini \n pursuing BE-CSE");// /n- new line
	System.out.println("Hello I'm Harini \r pursuing BE-CSE");// /r- carriage return
	System.out.println("Hello I'm Harini \t pursuing BE-CSE");// /t- tab
	System.out.println("Hello I'm Harini \b pursuing BE-CSE");// /b- Backspace
	System.out.println("Hello I'm Harini pursuing \f BE-CSE");// /f- form feed
	}
}
	
